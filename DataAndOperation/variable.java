
public class variable {
	public static void main(String[] args) {
		
		//문자
		char a='a'; //문자
		System.out.println(a);
		
		String b="Hello World"; //문자열
		System.out.println(b);
		
		//정수
		short c=2; //2byte(-32,768 ~ 32,767)	
		System.out.println(c);
		
		int d=4; //integer:4byte(2,147,483,648~2,147,483,647)
		System.out.println(d);
		
		long e=8; //8byte(-9,223,372,036,854,775,808~9,223,372,036,854,775,807)
		System.out.println(e);
		
		//실수(realNumber)
		double f=4.0;
		System.out.println(f);
		
		double g=8.0;
		System.out.println(g);		
		
		
		//변수 데이터 타입 지정 - 명시적으로 어떤 데이터 들어있는지 알려줌
			
	}

}
